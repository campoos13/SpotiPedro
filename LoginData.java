package persistencia;

/**
 *
 * @author josel
 */
public class LoginData {
    private final String login;
    private final String senha;

    public LoginData(String login, String senha) {
        this.login = login;
        this.senha = senha;
    }

    public String getLogin() {
        return login;
    }

    public String getSenha() {
        return senha;
    }
    
}
